

public class Obje {
	int x;
	int y;
	int width;
	int height;
	public Obje(int x,int y,int w,int h){
		this.x=x;
		this.y=y;
		width=w;
		height=h;
	}
	public int getX(){
		return x;
	}
	public int getY(){
		return y;
	}
	public int getW(){
		return width;
	}
	public int getH(){
		return height;
	}
	
	
}
